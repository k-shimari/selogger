	public static boolean hasType(int rawEventType) {
		switch (BASE_EVENT[rawEventType]) {
		case EVENT_FORMAL_PARAM:
		case EVENT_METHOD_NORMAL_EXIT:
		case EVENT_METHOD_EXCEPTIONAL_EXIT:
		case EVENT_THROW:
		case EVENT_CATCH:
		case EVENT_ACTUAL_PARAM:
		case EVENT_RETURN_VALUE_AFTER_CALL:
		case EVENT_GET_FIELD_RESULT:
		case EVENT_PUT_INSTANCE_FIELD:
		case EVENT_PUT_INSTANCE_FIELD_BEFORE_INITIALIZATION:
		case EVENT_PUT_STATIC_FIELD:
		case EVENT_ARRAY_LOAD_RESULT:
		case EVENT_ARRAY_STORE:
		case EVENT_ARRAY_LENGTH_RESULT:
		case EVENT_INSTANCEOF:
		case EVENT_CONSTANT_OBJECT_LOAD:
			return true;
		default:
			return false;
		}
	}
	
