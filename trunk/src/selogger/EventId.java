	static {
		for (int i=0; i<BASE_EVENT.length; ++i) {
			if (hasType(i)) {
				assert (i - BASE_EVENT[i] >= 0) && (i - BASE_EVENT[i] < 10); 
			} else {
				assert BASE_EVENT[i] == i: "BASE_EVENT is not consistent for " + i;
			}
		}
		
		for (int i=0; i<EVENT_NAMES.length; ++i) {
			String name = EVENT_NAMES[i];
			try {
				Field f = EventId.class.getField("EVENT_" + name.replace(':', '_'));
				assert i == f.getInt(null): "Invalid value is assigned EVENT_NAMES[" + i + "] = " + name;
			} catch (IllegalAccessException e) {
				assert false: "Cannot access static field " + name;
			} catch (NoSuchFieldException e) {
				assert false: "Invalid string is assigned in EVENT_NAMES[" + i + "] = " + name;
			} catch (SecurityException e) {
				// ignore the exception
			}
		}
		assert EVENT_NAMES[EVENT_CATCH].equals("CATCH");
		assert EVENT_NAMES[EVENT_CONSTANT_OBJECT_LOAD].equals("CONSTANT_OBJECT_LOAD");
	}
	
